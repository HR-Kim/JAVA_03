package com.sist;

public class Variable02 {

	public static void main(String[] args) {
		int num;
		num = 3;
		//변수 num의 값은 3입니다.
		System.out.println("변수 num의 값은 " + num + "입니다.");
        
		num = 5;
		System.out.println("변수 num의 값을 변경했습니다.");
        
		//변수 num의 값은 5입니다.
		System.out.println("변수 num의 값은 " + num + "입니다.");

	}

}
