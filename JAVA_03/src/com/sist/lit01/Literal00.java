package com.sist.lit01;

public class Literal00 {
	/**
	 * 문자 리터럴
	 * 1. 하나의 문자: 문자리터럴(character literal) ex)'A'
	 * 2. 문자열(스트링):
	 * @param args
	 */
	public static void main(String[] args) {
		System.out.println('A');//character literal
		System.out.println(' ');//' '
		System.out.println("Java 세상에 오신 것을 환영합니다!");//String literal
		System.out.println("");//공백문자 가능.
		System.out.println(123);
	}

}
